class Solution:
    def predictPartyVictory(self, senate: str) -> str:
        count = 0
        strlen = len(senate)
        index = 0
        while True:
            flag = 0
            while index < strlen:
                if senate[index] == 'D':
                    if flag % 2 == 0:
                        flag += 1
                    if count < 0:
                        senate = senate[0:index] + senate[index+1:]
                        index -= 1
                    count += 1

                elif senate[index] == 'R':
                    if flag == 0 or flag % 2 == 1:
                        flag += 2
                    if count > 0:
                        senate = senate[0:index] + senate[index + 1:]
                        index -= 1
                    count -= 1
                index += 1
                strlen = len(senate)

            index = index % strlen
            if strlen == 1 or flag < 3:
                win = senate[0]
                break
        if win == 'R':
            return 'Radiant'
        else:
            return 'Dire'

